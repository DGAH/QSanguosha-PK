--[[
	技能：惴恐
	描述：一名其他角色的回合开始时，若你已受伤，你可以与其拼点：若你赢，该角色跳过出牌阶段；若你没赢，该角色无视与你的距离，直到回合结束。
]]--
--[[
	技能：求援
	描述：每当你成为【杀】的目标时，你可以令一名除此【杀】使用者外的有手牌的其他角色正面朝上交给你一张手牌：若此牌不为【闪】，该角色也成为此【杀】的目标。
]]--
